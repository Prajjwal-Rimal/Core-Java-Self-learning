//- **Q19:** Write a program that prints the multiplication table of a given number
// using a `do-while` loop.

class chatgptloop3{
    public static void main(String[]args){

        int i=1;
        do{
            System.out.println("2 times "+ i+" = " + 2*i);
            i++;
        }while(i<=10);
    }
}