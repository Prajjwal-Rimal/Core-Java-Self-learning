//- **Q3:** Write a Java program that initializes variables
// using different types of literals (integer, floating-point, character, string, boolean).

class chatgptliterals1{
    public static void main(String[]args){
        int a = 10;
        float b = 10f;
        double c= 11;
        short d=2;
        byte e =127;
        long f =256l;// l should be capitla L and not l
        char g ='k';
        boolean h =true;
        String i = "hello";

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
        System.out.println(i);
    }
}